export class Book {

  public title: string;
  public subtitle: string;
  public author: string;
  public image: string;
  public id: string;

  constructor(
    title: string,
    subtitle: string,
    author: string,
    image: string,
    id: string
    ) {

    this.title = title;
    this.subtitle = subtitle;
    this.author = author;
    this.image = image;
    this.id = image;

  }

}
